create or replace procedure populate_db 
	is
	name varchar2(30);
	g_dat date;
	g_type varchar2(10);
	begin
		for iter in (select p.patient_name as name, p.General_ward_admission_date as g_dat, 
					p.patient_type as g_type from PATIENT_INPUT p
					order by p.General_ward_admission_date, p.patient_name)
		loop
			insert into GENERAL_WARD values(iter.name, iter.g_dat, iter.g_type);
		end loop;
	end;
/
exec populate_db;

